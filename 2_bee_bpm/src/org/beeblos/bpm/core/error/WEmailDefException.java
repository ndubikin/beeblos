public class WEmailDefException extends Exception {


	/**
	 * 
	 */
	private static final long serialVersionUID = -8324836221655596156L;

	/**
	 * 
	 */
	public WEmailDefException() {
		
	}

	/**
	 * @param arg0
	 */
	public WEmailDefException(String arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 */
	public WEmailDefException(Throwable arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public WEmailDefException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}

