package org.beeblos.bpm.core.error;

public class WStepTypeDefException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 24652353247973549L;

	/**
	 * 
	 */
	public WStepTypeDefException() {
		
	}

	/**
	 * @param arg0
	 */
	public WStepTypeDefException(String arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 */
	public WStepTypeDefException(Throwable arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public WStepTypeDefException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}
}
