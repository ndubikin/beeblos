package org.beeblos.bpm.core.error;

public class WUserDeviceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 9041970749138321399L;

	/**
	 * 
	 */
	public WUserDeviceException() {
		
	}

	/**
	 * @param arg0
	 */
	public WUserDeviceException(String arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 */
	public WUserDeviceException(Throwable arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public WUserDeviceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}
