package org.beeblos.security.auxiliar.error;

/**
 * @author Roger
 *
 */
public class TipoTerritorioException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 24652353247973549L;

	/**
	 * 
	 */
	public TipoTerritorioException() {
		
	}

	/**
	 * @param arg0
	 */
	public TipoTerritorioException(String arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 */
	public TipoTerritorioException(Throwable arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public TipoTerritorioException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}
