package org.beeblos.security.st.error;

/**
 * @author Roger
 *
 */
public class UsuarioFuncionesException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 24652353247973549L;

	/**
	 * 
	 */
	public UsuarioFuncionesException() {
		
	}

	/**
	 * @param arg0
	 */
	public UsuarioFuncionesException(String arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 */
	public UsuarioFuncionesException(Throwable arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public UsuarioFuncionesException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}
