package org.beeblos.bpm.core.error;

public class WStepWorkSequenceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 884828375248702357L;

	/**
	 * 
	 */
	public WStepWorkSequenceException() {
	}

	/**
	 * @param arg0
	 */
	public WStepWorkSequenceException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public WStepWorkSequenceException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public WStepWorkSequenceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}

