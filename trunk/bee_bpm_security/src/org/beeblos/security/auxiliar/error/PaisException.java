package org.beeblos.security.auxiliar.error;

public class PaisException extends Exception {

	private static final long serialVersionUID = -4177071033639715094L;

		/**
		 * 
		 */
		public PaisException() {
			
		}

		/**
		 * @param arg0
		 */
		public PaisException(String arg0) {
			super(arg0);
			
		}

		/**
		 * @param arg0
		 */
		public PaisException(Throwable arg0) {
			super(arg0);
			
		}

		/**
		 * @param arg0
		 * @param arg1
		 */
		public PaisException(String arg0, Throwable arg1) {
			super(arg0, arg1);
			
		}

}




