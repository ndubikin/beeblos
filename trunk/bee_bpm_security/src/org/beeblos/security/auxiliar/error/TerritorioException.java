package org.beeblos.security.auxiliar.error;

/**
 * @author Roger
 *
 */
public class TerritorioException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 24652353247973549L;

	/**
	 * 
	 */
	public TerritorioException() {
		
	}

	/**
	 * @param arg0
	 */
	public TerritorioException(String arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 */
	public TerritorioException(Throwable arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public TerritorioException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}
